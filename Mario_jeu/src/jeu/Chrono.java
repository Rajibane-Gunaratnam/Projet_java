package jeu;

//*********************************************************************************************************************
public class Chrono implements Runnable {
//**************************DECLARATION DE VARIABLE********************************************************************
	
	private final int PAUSE = 3;
	
//**************************METHODES***********************************************************************************
	public void run() {
		while(true) {
			FenetreJeu.niveau_1.repaint();
			try {Thread.sleep(PAUSE);} 
			catch (InterruptedException e) {}
		}	
	}
	
}
